/**
  * @brief This function handles USART3 global interrupt.
  */
void USART3_IRQHandler(void)
{
  /* USER CODE BEGIN USART3_IRQn 0 */

  /* USER CODE END USART3_IRQn 0 */
  HAL_UART_IRQHandler(&huart3);
  /* USER CODE BEGIN USART3_IRQn 1 */
  Bluetooth_data = rx_buf[0];

  switch (Bluetooth_data) 
  {
    case 0x00:  //ЩВГЕ
        Fore = 0; Back = 0; Left = 0; Right = 0; 
        break;
    case 0x01:  //ЧАНј
        Fore = 1; Back = 0; Left = 0; Right = 0;
        break;
    case 0x05:  //КѓЭЫ
        Fore = 0; Back = 1; Left = 0; Right = 0;
        break;
    case 0x03:  //гвзЊ
        Fore = 0; Back = 0; Left = 0; Right = 1;
        break;
    case 0x07:  //зѓзЊ          
        Fore = 0; Back = 0; Left = 1; Right = 0;
        break;
    case 0x02:  // д­ЕизѓзЊЃЈСНТжЗДзЊЃЉ
        Fore = 0; Back = 1; Left = 1; Right = 0;
        break;
    case 0x04:  // д­ЕигвзЊЃЈСНТжЗДзЊЃЉ
        Fore = 0; Back = 1; Left = 0; Right = 1;
        break;
    default:
        Fore = 0; Back = 0; Left = 0; Right = 0;
        break;
  }

  HAL_UART_Receive_IT(&huart3,rx_buf,1);
  /* USER CODE END USART3_IRQn 1 */
}

